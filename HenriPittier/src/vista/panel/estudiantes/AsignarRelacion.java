    private void btn_asignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_asignarActionPerformed
        if((!txt_CedulaEstudiante.getText().equals("")) && (!txt_CedulaRepresentante.getText().equals("")) && (cb_parentezco.getSelectedItem() != null)) {
            try {
                
                cedulaRepresentante = Integer.parseInt(txt_CedulaRepresentante.getText());
                Rep_Est repestRegistar = new Rep_Est(cedulaRepresentante, cedulaEstudiante, (String) cb_parentezco.getSelectedItem());
                ComunicacionREST comRest = new ComunicacionREST();
                Rep_Est repestRegistrado = comRest.registrarRepEst(repestRegistar);
                
                if (repestRegistrado.getError() == Registry.RESULTADO_CODIGO_BIEN){
                    final JPanel panel = new JPanel();
                    JOptionPane.showMessageDialog(panel, "Se realizo exitosamente la asignacion", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    final JPanel panel = new JPanel();
                    JOptionPane.showMessageDialog(panel, "No se pudo realizar la asignacion, intente nuevamente", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception ex) {
                Logger.getLogger(BorrarEstudiante.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else {
            final JPanel panel = new JPanel();
            JOptionPane.showMessageDialog(panel, "No debe dejar los campos vacios, intente nuevamente", "Error", JOptionPane.ERROR_MESSAGE);
        }
            
    }//GEN-LAST:event_btn_asignarActionPerformed


