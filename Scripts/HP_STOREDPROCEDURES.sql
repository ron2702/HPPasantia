/***************************************MANEJO DE USUARIOS**************************************/
CREATE OR REPLACE FUNCTION EMPLEADO_INICIARSESION(varchar(50), varchar(15)) RETURNS integer AS $$
DECLARE
 RESULT integer;

BEGIN
	IF ((SELECT COUNT(*) FROM EMPLEADO WHERE NOMBREUSUARIO = $1 AND CONTRASENA = $2) = 0) THEN

		RESULT := 204;
  	
  	ELSE
	
		RESULT := 200;
	
	END IF;
 	RETURN RESULT;
END;
$$ LANGUAGE plpgsql;


